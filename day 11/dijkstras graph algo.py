def graph(d,x):
    c={5:9999,8:9999,6:9999,9:9999,4:9999,7:9999,3:9999,2:9999}
    c[x]=0
    q=[x]
    v=[]
    while q:
        mi=9999
        for i in q:
            if c[i]<mi:
                mi=c[i]
                x=i
        for i in d[x]:
            if i[0] not in v:
                q.append(i[0])
                if c[i[0]]>i[1]+c[x]:
                    c[i[0]]=i[1]+c[x]
        v.append(q.pop(0))
    print(c)
